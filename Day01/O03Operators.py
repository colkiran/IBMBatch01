print("Arithmetic Operators".center(60, "-"))
print(f"Add : 10 + 3 = {10 + 3}")
print(f"Sub : 10 - 3 = {10 - 3}")
print(f"Mul : 10 * 3 = {10 * 3}")
print(f"Div : 10 / 3 = {10 / 3}")
print(f"floor : 10 // 3 = {10 // 3}")   # returns a integer
print(f"Mod : 10 % 3 = {10 % 3}")       # reminder
print(f"Power : 10 ** 3 = {10 ** 3}")

print("Augmentor".center(60, "-"))
# =, +=, -=, *=
x = 10
print(f"x :{x}")
x += 5      # x = x + 5
print(f"x :{x}")
x /= 3      # x = x / 5
print(f"x :{x}")
